from django import forms

EFFORT_CHOICES = (('dynamic', 'Dynamic'), ('max', 'Max'))
BOXFREE_CHOICES = (('free', 'Free'), ('box', 'Box'))
SUMO_CHOICES = (('sumo', 'Sumo'), ('conventional', 'Conventional'))

class SquatForm(forms.Form):
	effort_type = forms.ChoiceField(widget=forms.RadioSelect, choices=EFFORT_CHOICES)
	box_free = forms.ChoiceField(widget=forms.RadioSelect, choices=BOXFREE_CHOICES)
	bar_type = forms.CharField(required=False, max_length=60)
	bands_type = forms.CharField(required=False, max_length=60)
	chain_weight = forms.IntegerField(required=False, initial=0)
	movement_weight = forms.IntegerField(required=False, initial=0)
	movement_reps = forms.IntegerField(required=False, initial=0)

class DeadliftForm(forms.Form):
	sumo_conventional = forms.ChoiceField(widget=forms.RadioSelect, choices=SUMO_CHOICES)
	deficit = forms.BooleanField(required=False)
	block = forms.BooleanField(required=False)
	standard = forms.BooleanField(required=False)
	pin = forms.BooleanField(required=False)
	reverse = forms.BooleanField(required=False)
	movement_weight = forms.IntegerField(required=False, initial=0)
	movement_reps = forms.IntegerField(required=False, initial=0)
	deadlift_notes = forms.CharField(max_length=60,required=False)

